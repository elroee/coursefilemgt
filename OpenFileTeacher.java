public void GetFile(){
    Statement stat;
    ResultSet rs;
    String url;
    String un,up;
    un=Database.uname;
    up=Database.upass;
   
    try{
         Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
         url="jdbc:sqlserver://localhost:1433;databasename=courseManagement";
         Connection con = DriverManager.getConnection(url,un,up);
         stat=con.createStatement();
         String cmdtxt="SELECT FID,FName AS [File Name],TUName AS [Teacher's User Name],UploadDate FROM Files WHERE CourseID="+cid;
         rs = stat.executeQuery(cmdtxt);
         tblFiles.setModel(DbUtils.resultSetToTableModel(rs));
         rs = stat.executeQuery(cmdtxt);
         fileComboBox.removeAllItems();
         boolean exists;
         while(rs.next()){  
             exists = false;
             String item=String.valueOf(rs.getInt(1));
             for (int index = 0; index < fileComboBox.getItemCount() && !exists; index++) {
             if (item.equals(fileComboBox.getItemAt(index))) {
                exists = true;
             }
            
             }
              if(!exists)
                fileComboBox.addItem(item);
             }
         con.close();
    }catch (Exception ex){
         JOptionPane.showMessageDialog(null,ex.getMessage());
     }
     finally{

     }
}

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
         JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
         int r = jfc.showSaveDialog(null);
         if (r == JFileChooser.APPROVE_OPTION) {
                Statement stat;
                ResultSet rs;
                String url;
                byte[] fileBytes;
                String query;
                String fileName=jfc.getSelectedFile().getAbsolutePath();
                try {
                    Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                    url = "jdbc:sqlserver://localhost:1433;databaseName=courseManagement";
                    Connection con = DriverManager.getConnection(url, un, up);
                    query="Select FID,FPath,UPFile FROM Files WHERE FID="+Integer.valueOf(String.valueOf(fileComboBox.getSelectedItem()));
                    stat=con.createStatement();
                    rs=stat.executeQuery(query);
                    OutputStream targetFile;
                    if (rs.next()) {
                     fileBytes = rs.getBytes(3);
                     if(rs.getString(2).matches("%.pdf"))
                        targetFile=  new FileOutputStream(fileName+".pdf");
                     else
                         targetFile=  new FileOutputStream(fileName+".ppt");
                     targetFile.write(fileBytes);
                     targetFile.close();
            }
                     
                    JOptionPane.showMessageDialog(null, "SAVED!");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,ex.getMessage());
                    //jl2.setText("ERROR!");
                }
            } 

    }//GEN-LAST:event_btnSaveActionPerformed

