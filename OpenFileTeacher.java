    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(fileComboBox.getSelectedIndex()==-1)
        {
            JOptionPane.showMessageDialog(null, "No file selected");
        }
        else
        {
                  Statement stat;
    
    String url;
    String un,up;
    un=Database.uname;
    up=Database.upass;
    int chosenid=Integer.valueOf(String.valueOf(fileComboBox.getSelectedItem()));
    try{
         Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
         url="jdbc:sqlserver://localhost:1433;databasename=courseManagement";
         Connection con = DriverManager.getConnection(url,un,up);
         stat=con.createStatement();
         String cmdtxt="SELECT FID, Courses.CourseID, Courses.CourseName, Courses.Instructor "+
                        "FROM Files JOIN Courses ON Courses.CourseID=Files.CourseID WHERE FID="+chosenid;
         ResultSet rs=stat.executeQuery(cmdtxt);
         rs.next();
         int cid=rs.getInt(2);
         String cname=rs.getString(3);
         String tname=rs.getString(4);
         cmdtxt="SELECT TblStudent.SID, TblStudent.SUName FROM TakesCourses "+
                 "JOIN TblStudent ON TakesCourses.SID=TblStudent.SID WHERE CourseID="+cid;
         rs=stat.executeQuery(cmdtxt);
         while(rs.next()){
             Database.AddSNotification(rs.getInt(1),tname , "File Deleted from Course "+cname);
         }
         cmdtxt="DELETE FROM Files WHERE FID="+chosenid;
         stat.executeUpdate(cmdtxt);
         JOptionPane.showMessageDialog(null, "DELETED!");
         con.close();
    }catch (Exception ex){
         JOptionPane.showMessageDialog(null,ex.getMessage());
     }finally{
        GetFile();
    }

        }
  
    }//GEN-LAST:event_btnDeleteActionPerformed

